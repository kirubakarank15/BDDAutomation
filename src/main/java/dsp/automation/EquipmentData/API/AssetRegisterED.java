package dsp.automation.EquipmentData.API;

public class AssetRegisterED {

	private String serialNumber;
	private String manufacturerCode;
	private String prodWorkCode;
	private String model;
	private String manufacturingYear;
	private String ownerType;
	private String dealerCustomerNumber;
	private String ownerStartDate;
	private String equipmentId;
	private String vin;
	private String dealerCode;
	
	
	public String getSerialNumber() {
		return serialNumber;
	}
	public void setSerialNumber(String serialNumber) {
		this.serialNumber = serialNumber;
	}
	public String getManufacturerCode() {
		return manufacturerCode;
	}
	public void setManufacturerCode(String manufacturerCode) {
		this.manufacturerCode = manufacturerCode;
	}
	public String getProdWorkCode() {
		return prodWorkCode;
	}
	public void setProdWorkCode(String prodWorkCode) {
		this.prodWorkCode = prodWorkCode;
	}
	public String getModel() {
		return model;
	}
	public void setModel(String model) {
		this.model = model;
	}
	public String getManufacturingYear() {
		return manufacturingYear;
	}
	public void setManufacturingYear(String manufacturingYear) {
		this.manufacturingYear = manufacturingYear;
	}
	public String getOwnerType() {
		return ownerType;
	}
	public void setOwnerType(String ownerType) {
		this.ownerType = ownerType;
	}
	public String getDealerCustomerNumber() {
		return dealerCustomerNumber;
	}
	public void setDealerCustomerNumber(String dealerCustomerNumber) {
		this.dealerCustomerNumber = dealerCustomerNumber;
	}
	public String getOwnerStartDate() {
		return ownerStartDate;
	}
	public void setOwnerStartDate(String ownerStartDate) {
		this.ownerStartDate = ownerStartDate;
	}
	public String getEquipmentId() {
		return equipmentId;
	}
	public void setEquipmentId(String equipmentId) {
		this.equipmentId = equipmentId;
	}
	public String getVin() {
		return vin;
	}
	public void setVin(String vin) {
		this.vin = vin;
	}
	public String getDealerCode() {
		return dealerCode;
	}
	public void setDealerCode(String dealerCode) {
		this.dealerCode = dealerCode;
	}
	
	
	
}
